Story = "once upon a time there was a superhero named krrish"

# string functions

# len : gives the length of the string
print(len(Story))

# endswith: add the given string to the last or at the end 
print(Story.endswith("krrish"))

# count : counts the number of words or alphabet occuring repeatedly
print(Story.count("n"))
 
# capitalize : return the first word capital
print(Story.capitalize())

# find : searches the given string if available
print(Story.find("upon"))

# replace : 
print(Story.replace("krrish","krishna"))
